using BSDCPolls.Api.Data.Entities;
using BSDCPolls.Api.Data.Infrastructure;
using BSDCPolls.Api.Data.ValueConverters;
using BSDCPolls.Contracts.Enums;
using Microsoft.EntityFrameworkCore;

namespace BSDCPolls.Api.Data;

/// <summary>
/// EF Core database context for the BSDCPolls platform.
/// Uses lazy-loading proxies (all navigation properties must be <c>virtual</c>).
/// Audit fields are stamped automatically by <see cref="AuditInterceptor"/>.
/// </summary>
public class BsdcPollsDbContext : DbContext
{
    /// <summary>Initialises a new instance with the given options.</summary>
    /// <param name="options">DbContext configuration options.</param>
    public BsdcPollsDbContext(DbContextOptions<BsdcPollsDbContext> options)
        : base(options)
    {
    }

    /// <summary>All registered users (no PII — username + Supabase sub only).</summary>
    public DbSet<ApplicationUser> ApplicationUsers => Set<ApplicationUser>();

    /// <summary>Historical username records for reuse prevention.</summary>
    public DbSet<UsernameHistory> UsernameHistories => Set<UsernameHistory>();

    /// <summary>Per-user privacy settings.</summary>
    public DbSet<UserPrivacySettings> UserPrivacySettings => Set<UserPrivacySettings>();

    /// <summary>Allowlist entries for users with AllowlistOnly invite permission.</summary>
    public DbSet<InviteAllowlistEntry> InviteAllowlistEntries => Set<InviteAllowlistEntry>();

    /// <summary>Live poll sessions.</summary>
    public DbSet<Poll> Polls => Set<Poll>();

    /// <summary>Questions within a poll session.</summary>
    public DbSet<PollQuestion> PollQuestions => Set<PollQuestion>();

    /// <summary>Selectable answer choices for a poll question.</summary>
    public DbSet<PollAnswerOption> PollAnswerOptions => Set<PollAnswerOption>();

    /// <summary>Participant votes on poll questions.</summary>
    public DbSet<PollSubmission> PollSubmissions => Set<PollSubmission>();

    /// <summary>Structured surveys with JSONB question trees.</summary>
    public DbSet<Survey> Surveys => Set<Survey>();

    /// <summary>Respondent answers to surveys.</summary>
    public DbSet<SurveyResponse> SurveyResponses => Set<SurveyResponse>();

    /// <summary>PDF uploads for document-upload questions.</summary>
    public DbSet<SurveyDocument> SurveyDocuments => Set<SurveyDocument>();

    /// <summary>Directed invitations to polls or surveys.</summary>
    public DbSet<Invitation> Invitations => Set<Invitation>();

    /// <summary>In-app notifications generated by invitations.</summary>
    public DbSet<Notification> Notifications => Set<Notification>();

    /// <summary>Append-only change history for all AuditableEntity subclasses.</summary>
    public DbSet<AuditLog> AuditLogs => Set<AuditLog>();

    /// <inheritdoc />
    protected override void OnModelCreating(ModelBuilder modelBuilder)
    {
        base.OnModelCreating(modelBuilder);

        ConfigureApplicationUser(modelBuilder);
        ConfigureUsernameHistory(modelBuilder);
        ConfigureUserPrivacySettings(modelBuilder);
        ConfigureInviteAllowlistEntry(modelBuilder);
        ConfigurePoll(modelBuilder);
        ConfigurePollQuestion(modelBuilder);
        ConfigurePollAnswerOption(modelBuilder);
        ConfigurePollSubmission(modelBuilder);
        ConfigureSurvey(modelBuilder);
        ConfigureSurveyResponse(modelBuilder);
        ConfigureSurveyDocument(modelBuilder);
        ConfigureInvitation(modelBuilder);
        ConfigureNotification(modelBuilder);
        ConfigureAuditLog(modelBuilder);

        SeedSystemSentinel(modelBuilder);
    }

    private static void ConfigureApplicationUser(ModelBuilder modelBuilder)
    {
        modelBuilder.Entity<ApplicationUser>(e =>
        {
            e.ToTable("application_users");
            e.HasKey(u => u.Id);
            e.HasIndex(u => u.Uid).IsUnique();
            e.HasIndex(u => u.Username).IsUnique();
            e.HasIndex(u => u.SupabaseUserId).IsUnique();

            // Self-referential audit FKs — restrict to avoid cascades
            e.HasOne(u => u.CreatedBy)
                .WithMany()
                .HasForeignKey(u => u.CreatedById)
                .OnDelete(DeleteBehavior.Restrict);

            e.HasOne(u => u.UpdatedBy)
                .WithMany()
                .HasForeignKey(u => u.UpdatedById)
                .OnDelete(DeleteBehavior.Restrict);
        });
    }

    private static void ConfigureUsernameHistory(ModelBuilder modelBuilder)
