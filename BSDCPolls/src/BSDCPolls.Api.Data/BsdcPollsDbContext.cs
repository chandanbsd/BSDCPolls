    private static void ConfigureAuditLog(ModelBuilder modelBuilder)
    {
        modelBuilder.Entity<AuditLog>(e =>
        {
            e.ToTable("audit_logs");
            e.HasKey(a => a.Id);

            e.Property(a => a.Id)
                .ValueGeneratedOnAdd();

            e.Property(a => a.Payload)
                .HasColumnType("jsonb");

            e.HasOne(a => a.PerformedBy)
                .WithMany()
                .HasForeignKey(a => a.PerformedById)
                .OnDelete(DeleteBehavior.Restrict);
        });
    }

    private static void SeedSystemSentinel(ModelBuilder modelBuilder)
    {
        // System sentinel user — Id=1, used as CreatedById/UpdatedById during bootstrapping
        // and by AuditInterceptor when no authenticated user is in scope.
        var sentinelUid = new Guid("00000000-0000-0000-0000-000000000001");
        var epoch = new DateTime(2024, 1, 1, 0, 0, 0, DateTimeKind.Utc);

        modelBuilder.Entity<ApplicationUser>().HasData(new
        {
            Id = 1,
            Uid = sentinelUid,
            Username = "system",
            SupabaseUserId = "SYSTEM",
            IsActive = false,
            CreatedOn = epoch,
            CreatedById = 1,
            UpdatedOn = epoch,
            UpdatedById = 1,
        });
    }
